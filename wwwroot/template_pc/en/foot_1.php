    $('#head-verifyCode').bind('input propertychange', function () {
        var p = $('#head-verifyCode').val();
        if ($('#head-verifyCode').attr('data-value') == 1) {
            if ($('#head-verifyCode').val() == '') {
                $('#head-verifyCode').css("border-color", "red");
                $('#head-verifyCode').attr('data-valid', '0');
            } else {
                if (p.length != 4) {
                    $('#head-verifyCode').css("border-color", "red");
                    $('#head-verifyCode').attr('data-valid', '442');
                }
            }
        }

        if (p.length == 4) {
            $.post("/reg/comparison", {
                verifyCode: $('#head-verifyCode').val()
            }, function (result) {
                if (result == 1) {
                    $('#head-verifyCode').css("border-color", "green");
                    $('#head-verifyCode').attr('data-valid', '240');
                    $('#head-verifyCode').attr('data-value', '1');
                } else {
                    $('#head-verifyCode').css("border-color", "red");
                    $('#head-verifyCode').attr('data-valid', '442');
                }
            }).fail(function (xhr, errorText, errorType) {
                $.notifyBar({cssClass: "dg-notify-error", html: "error:404", position: "bottom"})
            });
        }
    });


    $('#Login').click(function () {
        $('#waring').css("display", "none");
        $(this).data('bind', $(this).data('bind') + 1);
        $.post("/reg/click", {
            code_switch: $('#Login').data('bind')
        }).fail(function (xhr, errorText, errorType) {
            $.notifyBar({cssClass: "dg-notify-error", html: "error:404", position: "bottom"})
        });
        if ($('#head-email').val() == '') {
            $('#error_error').empty();
            $("#waring").css("display", "block");
            $('#error_error').append('Your email not null');
            $('#head-email').css("border-color", "red");
            return false;
        } else {
            if (!(/^([\w-_]+(?:\.[\w-_]+)*)@((?:[a-z0-9]+(?:-[a-zA-Z0-9]+)*)+\.[a-z]{2,6})$/i.test($('#head-email').val()))) {
                $('#error_error').empty();
                $("#waring").css("display", "block");
                $('#error_error').append('Your email address is invalid');
                $('#head-email').css("border-color", "red");
                return false;
            }

        }

        if ($('#head-password').val() == '') {
            $('#error_error').empty();
            $("#waring").css("display", "block");
            $('#error_error').append('Your password not null');
            $('#head-password').css("border-color", "red");
            return false;
        } else {
            if (!/^[a-zA-Z0-9_]{6,20}$/i.test($('#head-password').val())) {
                $('#error_error').empty();
                $("#waring").css("display", "block");
                $('#error_error').append('Your password is invalid');
                $('#head-password').css("border-color", "red");
                return false;
            }
        }
        if ($('#head-verifyCode').attr('data-valid') != 240) {
            $('#error_error').empty();
            $("#waring").css("display", "block");
            $('#error_error').append('Verification code error');
            return false
        }

        if ($("#head-verifyCode-div").attr('style') == 'display:none') {
            if (parseInt($('#Login').data('bind')) == 4) {
                //alert($('#Login').data('bind'));
                $("#head-verifyCode-div").css("display", "block");
                $('#head-verifyCode').attr('data-valid', '0');
                $('#waring').css("display", "none");
                return false;

            }
        }

        $.post("<?php echo site_url('reg/login'); ?>", {
            myEmail: $('#head-email').val(),
            myPassword: $('#head-password').val(),
            verifyCode: $('#head-verifyCode').val()
        }, function (result) {
            var result = $.parseJSON(result);
            if (result.success) {
                location.reload();
            } else {
                $('#waring').css('display', 'block');
                $("#error_error").html('login error');

                if ($('#head-verifyCode-div').css('display') === 'block') {
                    $('#head-verifyCode').val('');
                    $('#Login_Img').attr('src', '/reg/vcode?k=' + Math.random());
                }
            }
        }).fail(function (xhr, errorText, errorType) {
            $.notifyBar({cssClass: "dg-notify-error", html: "error:404", position: "bottom"})
        });
    });


    $("#head-email,#head-password").focus(function () {

    $("#subscription-button-foot").click(function () {

        if ($('#subscription-input-foot').val() == '') {
            $.notifyBar({cssClass: "dg-notify-error", html: "Your email not null", position: "bottom"});
        } else {
            if (!(/^([\w-_]+(?:\.[\w-_]+)*)@((?:[a-z0-9]+(?:-[a-zA-Z0-9]+)*)+\.[a-z]{2,6})$/i.test($('#subscription-input-foot').val()))) {
                $.notifyBar({cssClass: "dg-notify-error", html: "Your email address is invalid", position: "bottom"});
            } else {
                $.post("/subscription/insert", {
                    email: $('#subscription-input-foot').val()
                }, function (result) {
                    var result = $.parseJSON(result);
                    if (result.status) {
                        $.notifyBar({cssClass: "dg-notify-success", html: "Success", position: "bottom"});
                        $('#subscription-input-foot').reset();
                    } else {
                        $.notifyBar({cssClass: "dg-notify-error", html: "The existing email!", position: "bottom"});
                    }
                }).fail(function (xhr, errorText, errorType) {
                    $.notifyBar({cssClass: "dg-notify-error", html: "error:404", position: "bottom"})
                });
            }

        }
    });
